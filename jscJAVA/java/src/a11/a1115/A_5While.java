package a11.a1115;


public class A_5While {
    public static void main(String[] args) {
        //1~10까지 공백을 두고 출력하시오

        int a = 1;
        // while문의 조건 :while(조건){반복 실행문}

        while (a<=10) {
            System.out.print(a++ + " "); //괄호 안 내용은 (a = a + 1 "/br") 이라는 의미 
        }
    }
}
